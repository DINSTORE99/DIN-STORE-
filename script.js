const overlay = document.querySelector('.overlay');
const modal = document.querySelector('.modal');
const modalTitle = modal?.querySelector('h2');
const modalPrice = modal?.querySelector('.price');
const modalLink = modal?.querySelector('a');
const modalDesc = modal?.querySelector('.desc');

/**
 * Fungsi buka modal sesuai ID produk
 */
function openModal(productId) {
  // Gabungkan semua produk
  const allProducts = [
    ...sgProducts,
    ...idProducts,
    ...vpsProducts,
    ...injectProducts,
    ...sosmedProducts,
    ...akrab
  ];

  const product = allProducts.find(p => p.id === productId);
  if (!product) return;

  // Isi data ke modal
  modalTitle.textContent = product.title || "Produk Tidak Dikenal";
  modalDesc.innerHTML = product.desc
    ? `<p>${product.desc}</p>` +
      (product.features
        ? `<ul>${product.features.map(f => `<li>${f}</li>`).join('')}</ul>`
        : '')
    : '';
  modalPrice.textContent = product.price || '';

  // Buat link WA otomatis
  const waText = encodeURIComponent(
    `Halo kak, saya ingin pesan ${product.title} (${product.price}).`
  );
  modalLink.href = `https://wa.me/${WA_NUMBER}?text=${waText}`;

  overlay.classList.add('show');
}

/**
 * Tutup modal
 */
function closeModal() {
  overlay.classList.remove('show');
}

/**
 * Tutup modal jika klik luar
 */
overlay.addEventListener('click', (e) => {
  if (e.target === overlay) closeModal();
});

